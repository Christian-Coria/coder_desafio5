#creamos la funcion y solicitamos dos argumentos
def relacion(num1,num2):
    #fijamos las condiciones dentro del modulo if, elif, else
    if num1 > num2:
        print(1)
    elif num1 < num2:
        print(-1)
    else:
        print(0)
 
#Comprueba la relación entre los números: '5 y 10', '10 y 5' y '5 y 5'
#llamamos a la funcion pasando los dos argumentos
 
relacion(5,10)
relacion(10,5)
